/*
题目内容：时间换算
UTC是世界协调时，BJT是北京时间，UTC时间相当于BJT减去8。现在，你的程序要读入一个整数，
表示BJT的时和分。整数的个位和十位表示分，百位和千位表示小时。如果小时小于10，则没有千位部分；
如果小时是0，则没有百位部分；如果小时不是0而分小于10分，需要保留十位上的0；如果小时是0而分小于10分的，
则不需要保留十位上的0。如1124表示11点24分，而905表示9点5分，36表示0点36分，7表示0点7分。

有效的输入范围是0到2359，即你的程序不可能从测试服务器读到0到2359以外的输入数据。

你的程序要输出这个时间对应的UTC时间，输出的格式和输入的相同，即输出一个整数，表示UTC的时和分。
整数的个位和十位表示分，百位和千位表示小时。如果小时小于10，则没有千位部分；如果小时是0，则没有百位部分；
如果小时不是0而分小于10分，需要保留十位上的0；如果小时是0而分小于10分的，则不需要保留十位上的0。

提醒：要小心跨日的换算。

输入格式:

一个整数，表示BJT的时和分。整数的个位和十位表示分，百位和千位表示小时。如果小时小于10，
则没有千位部分；如果小时是0，则没有百位部分；如果小时不是0而分小于10分，需要保留十位上的0；
如果小时是0而分小于10分的，则不需要保留十位上的0。

输出格式：

一个整数，表示UTC的时和分。整数的个位和十位表示分，百位和千位表示小时。如果小时小于10，则没有千位部分；
如果小时是0，则没有百位部分；如果小时不是0而分小于10分，需要保留十位上的0；如果小时是0而分小于10分的，
则不需要保留十位上的0。
*/
#include <stdio.h>

int main()
{
    int bjt_time;
    printf("请输入BJT时间（格式：HHMM）：");
    scanf("%d", &bjt_time);

    // 提取小时和分钟
    int hours = bjt_time / 100;   // 获取小时部分
    int minutes = bjt_time % 100; // 获取分钟部分

    // 将BJT时间转换为UTC时间
    hours -= 8; // UTC = BJT - 8

    // 处理跨日的情况
    if (hours < 0)
    {
        hours += 24; // 如果小时小于0，说明跨日，需加24
    }

    // 输出UTC时间
    if (hours == 0)
    {
        // 小时为0，分钟小于10时不保留十位上的0
        if (minutes < 10)
        {
            printf("%d\n", minutes);
        }
        else
        {
            printf("%d\n", minutes);
        }
    }
    else
    {
        // 小时不为0，分钟小于10时保留十位上的0
        if (minutes < 10)
        {
            printf("%d0%d\n", hours, minutes);
        }
        else
        {
            printf("%d%d\n", hours, minutes);
        }
    }

    return 0;
}